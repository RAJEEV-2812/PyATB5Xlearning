# write a program to take the 2 user input
# then sum of the number
# mul->*
# Div_>/

# LOgic Building
# step 1
# I/P-> num1, num2->int
# O/P-> sum - int, sub ->int, Div-> Float

num1 = input("Enter the num 1")
num2 = input("Enter the num 2")

num1 = int(num1)
num2 = int(num2)

print(type(num1))
print(type(num2))


# step 2 / Rough Logic
# Sum +, -, /, *

# Step 3
sum = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2

print("sum is ->", sum)
print("sub is ->", sub)
print("Mul is ->", mul)
print("Div is ->", div)
